# Obtaining mortgage principle in $
mortgage_principle = int(input("what is your mortgage principle payment?\n"))

# Obtaining the the interest payment

mortgage_interest = int(input("what is your mortgage interest payment?\n"))

# Adding mortgage principle and interest to get principle & interest
mortgage_principle_and_interest = mortgage_principle + mortgage_interest

# Printing the mortgage payment

print(f"your mortgage payment is ${mortgage_principle_and_interest}")
